function omid() {
    var omidinfo = document.getElementById("omid-info");
    var omiddots = document.getElementById("omid-dots");
    var omidbtn = document.getElementById("omid-btn");

    if (omiddots.style.display === "none") {
        omiddots.style.display = "inline";
        omidbtn.innerHTML = "ادامه";
        omidinfo.style.display = "none";
    } else {
        omiddots.style.display = "none";
        omidbtn.innerHTML = "بستن";
        omidinfo.style.display = "inline";
    }
}
